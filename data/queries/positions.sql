SELECT BI_PLANNEDPOSITION.PLANNEDPOSITIONID, 
BI_PLANNEDPOSITION.POSITIONGRADE, 
BI_PLANNEDPOSITION.POSITIONTITLE, 
-- Replace new line characters
replace(BI_PLANNEDPOSITION.INCUMBENT_NAME, CHR(10), ' '), 
BI_PLANNEDPOSITION.OFFICEID, 
BI_PLANNEDPOSITION.EXISTINGPOSITION, 
BI_PLANNEDPOSITION.POSITIONTYPE, 
BI_PLANNEDPOSITION.REPORTSTO_ID, 
BI_PLAN.PLANID, 
BI_PLAN.OPERATIONID 
FROM BI_PLANNEDPOSITION
INNER JOIN BI_PLAN ON BI_PLANNEDPOSITION.PLANID = BI_PLAN.PLANID
