SELECT
  SUM(VALUE) AS VALUE,
  PPG_CODE,
  BI_OPERATION.OPERATIONID,
  ORIGPOPGROUP_ID AS PPGID,
  YEAR
FROM PSR_PPG_POPDETAILS
JOIN BI_PRJPOPULATIONGROUP ON BI_PRJPOPULATIONGROUP.MSRPCODE = PSR_PPG_POPDETAILS.PPG_CODE
JOIN BI_PLAN ON BI_PRJPOPULATIONGROUP.PLANID = BI_PLAN.PLANID
JOIN BI_OPERATION ON BI_OPERATION.OPERATIONID = BI_PLAN.OPERATIONID
GROUP BY
  PPG_CODE,
  BI_OPERATION.OPERATIONID,
  ORIGPOPGROUP_ID,
  YEAR
